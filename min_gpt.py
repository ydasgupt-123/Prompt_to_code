class NewGELU(nn.Module):
  def forward(self, x):
    return 0.5 * x * (1.0 + torch.tanh(math.sqrt(2.0 / math.pi) * (x + 0.044715 * torch.pow(x, 3.0))))

class RotaryPositionalEmbedding(nn.Module):
  def __init__(self, d: int, base: int = 10000):
    super().__init__()
    self.d = d

    inv_frequency = 1.0/(base**(torch.arange(0,d,2).float()/d)) ## The theta values will be equal to 10000^(2i/d). We use d/2 because we will be following pair-wise rotation of dimensions
    self.register_buffer("inv_frequency",inv_frequency)

    self.cos_cached = None
    self.sin_cached = None

  def _build_cache(self, x: torch.Tensor):
    """
    Builds a cache for cos and sin values based on the sequence length of x.
    x shape: [batch, seq_len, num_heads, head_dim] or [batch, seq_len, head_dim]
    """
    seq_len = x.shape[1]

    # Only rebuild cache if sequence length increases or cache is empty
    if self.cos_cached is None or seq_len > self.cos_cached.shape[1]:
    # Create position indices [0, 1, ..., seq_len-1]
      t = torch.arange(seq_len, device=x.device).type_as(self.inv_frequency)

      # Outer product to get matrix of (pos * theta)
      # freqs shape: [seq_len, d/2]
      freqs = torch.einsum("i,j->ij", t, self.inv_frequency)

      # Repeat frequencies for the 'rotate half' trick: [cos(f1), cos(f2), cos(f1), cos(f2)]
      # emb shape: [seq_len, d]
      emb = torch.cat((freqs, freqs), dim=-1)

      # Cache shape: [1, seq_len, 1, d] to allow broadcasting across batch and heads
      self.cos_cached = emb.cos()[None, :, None, :]
      self.sin_cached = emb.sin()[None, :, None, :]

  @staticmethod
  def _rotate_half(x: torch.Tensor):
    x1, x2 = x[..., :x.shape[-1]//2], x[..., x.shape[-1]//2:]
    return torch.cat((-x2, x1), dim=-1)

  def forward(self, x: torch.Tensor):
    if self.cos_cached is None:
      self._build_cache(x)
    seq_len = x.shape[1]
    cos = self.cos_cached[:, :seq_len, :, :].to(x.device)
    sin = self.sin_cached[:, :seq_len, :, :].to(x.device)

    return (x * cos) + (RotaryPositionalEmbedding._rotate_half(x) * sin)

class CausalSelfAttention(nn.Module):
  def __init__(self, config):
    super().__init__()
    assert config.n_embd % config.n_head == 0 ## This line of the code will check dimensions (example 768 or 512) and compare it with number of heads to ensure there is no embeddings left out

    self.n_head = config.n_query_head
    self.n_embd = config.n_embd

    # key, query, value projections for all heads, but in a batch
    self.q_proj = nn.Linear(config.n_embd, config.n_embd)
    self.k_proj = nn.Linear(config.n_embd, config.n_embd)
    self.v_proj = nn.Linear(config.n_embd, config.n_embd)

    # Output projection
    self.out_proj = nn.Linear(config.n_embd, config.n_embd)

    #Regularization
    self.attn_dropout = nn.Dropout(config.attn_pdrop)
    self.resid_dropout = nn.Dropout(config.resid_pdrop)

    #Causal Mask which is applied to ensure that attention is applied only on the left side of the network
    self.register_buffer("bias", torch.tril(torch.ones(config.block_size, config.block_size)).view(1, 1, config.block_size, config.block_size))

    self.rope = config.rope
    if self.rope:
      self.rotary_emb = RotaryPositionalEmbedding(config.n_embd)

  def forward(self, x):
    b, t, n_embd = x.size() # Batch size, Sequence_length and Embedding Dimensionality

    # Calculating the query, Key and Value based on X
    q = self.q_proj(x) # (b, t, n_embd)
    k = self.k_proj(x) # (b, t, n_embd)
    v = self.v_proj(x) # (b, t, n_embd)

    #split the embedding dimension (n_embd) across the number of heads by introducing an additional 'h' dimension
    # reshape the query, key, value tensors to increase efficacy of matrix multiplication
    # b=batch size, t=sequence length, h= number of heads, d = n_embd/heads
    q=rearrange(q, 'b t (h d) -> b h t d', h=self.n_head)
    k=rearrange(k, 'b t (h d) -> b h t d', h=self.n_head)
    v=rearrange(v, 'b t (h d) -> b h t d', h=self.n_head)

    if self.rope:
      q = self.rotary_emb(q)
      k = self.rotary_emb(k)

    #track the memory used by the model
    torch.cuda.empty_cache()
    start_memory = torch.cuda.memory_allocated()

    #compute the square root of (n_embd/ number of heads) to scale the dot product

    scale = math.sqrt(k.size(-1))

    #Calculate the attention scores with the query and Key
    att = einsum(q, k, 'b h q d, b h k d -> b h q k') / scale
    att = att.masked_fill(self.bias[:,:,:t,:t] == 0, float('-inf'))
    att = F.softmax(att, dim=-1)
    att = self.attn_dropout(att)

    # Matrix multiplication of Attention scores and value
    y=einsum(att, v, 'b h q t, b h t d -> b h q d')
    end_memory = torch.cuda.memory_allocated()

    # Rearrange the output tensor to (batch size, sequence length and n_embd)

    y = rearrange(y, 'b h q d -> b q (h d)')

    #output projection
    y = self.resid_dropout(self.out_proj(y))
    return y, end_memory - start_memory

class GroupedQueryAttention(nn.Module):
  def __init__(self, config):
    super().__init__()
    assert config.n_embd % config.n_head == 0 # The embedding dimension is divisible by the number of query heads and key-value heads.
    self.n_head = config.n_query_head
    self.n_kv_head = config.n_kv_head  # New parameter (e.g., 8 if n_head is 32)
    self.head_dim = config.n_embd // self.n_head
    self.n_groups = self.n_head // self.n_kv_head

    # Query, Key & Value for all heads but in a batch
    self.q_proj = nn.Linear(config.n_embd, config.n_embd)
    #K & V project into a smaller subspace for GQA
    self.k_proj = nn.Linear(config.n_embd, self.n_kv_head * self.head_dim)
    self.v_proj = nn.Linear(config.n_embd, self.n_kv_head * self.head_dim)

    # Output projection
    self.out_proj = nn.Linear(config.n_embd, config.n_embd)

    # Regularization
    self.attn_dropout = nn.Dropout(config.attn_pdrop)
    self.resid_dropout = nn.Dropout(config.resid_pdrop)

    #Causal Mask which is applied to ensure that attention is applied only on the left side of the network
    self.register_buffer("bias", torch.tril(torch.ones(config.block_size, config.block_size)).view(1, 1, config.block_size, config.block_size))

    self.rope = config.rope
    if self.rope:
      self.rotary_emb = RotaryPositionalEmbedding(config.n_embd)

  def forward(self, x):

    b, t, _ = x.size()

    q = self.q_proj(x) # (b, t, n_head * head_dim)
    k = self.k_proj(x) # (b, t, n_kv_head * head_dim)
    v = self.v_proj(x) # (b, t, n_kv_head * head_dim)

    # Reshape heads
    q = rearrange(q, 'b t (h d) -> b h t d', h=self.n_head)
    k = rearrange(k, 'b t (h d) -> b h t d', h=self.n_kv_head)
    v = rearrange(v, 'b t (h d) -> b h t d', h=self.n_kv_head)

    # NEW: Repeat K and V heads to match Q head count
    # This aligns the groups so einsum works correctly
    k = torch.repeat_interleave(k, self.n_groups, dim=1)
    v = torch.repeat_interleave(v, self.n_groups, dim=1)

    if self.rope:
        q = self.rotary_emb(q)
        k = self.rotary_emb(k)

    # Use PyTorch's optimized attention for better performance in 2026
    # This automatically handles causal masking and scaling
    y = F.scaled_dot_product_attention(
        q, k, v,
        attn_mask=None,
        dropout_p=self.attn_dropout.p if self.training else 0,
        is_causal=True
    )

    y = rearrange(y, 'b h t d -> b t (h d)')
    return self.resid_dropout(self.out_proj(y))

class Block (nn.Module):
  def __init__(self, config):
    super().__init__()
    self.ln_1 = nn.LayerNorm(config.n_embd)
    if config.n_query_head != config.n_kv_head:
      self.attn = GroupedQueryAttention(config)
    else:
      self.attn = CausalSelfAttention(config)
    self.ln_2 = nn.LayerNorm(config.n_embd)
    self.mlp = nn.ModuleDict(dict(
        c_fc = nn.Linear(config.n_embd, 4 * config.n_embd),
        c_proj = nn.Linear(4 * config.n_embd, config.n_embd),
        act = NewGELU(),
        dropout = nn.Dropout(config.resid_pdrop),
    ))
    m=self.mlp
    self.mlpf = lambda x: m.dropout(m.c_proj(m.act(m.c_fc(x)))) # MLP forward

  def forward(self, x):
    start_time = time.time()
    # Fixed typo: self.ln1 -> self.ln_1 and self.ln2 -> self.ln_2
    attn_comp, mem_consumed = self.attn(self.ln_1(x))
    end_time = time.time()
    x = x + attn_comp
    x = x + self.mlpf(self.ln_2(x))
    return x, end_time - start_time, mem_consumed

class GPT(nn.Module):
  @staticmethod
  def get_default_config():
    C = CfgNode() # Changed to CfgNode as defined earlier
    #Either the model type or (n_layer, n_head or n_config must be given in the code)
    C.model_type = 'gpt'
    C.n_layer = None
    C.n_head = None
    C.n_embd = None
    # These options must be externally inputted
    C.vocab_size = None # Changed from n_vocab to vocab_size for consistency
    C.block_size = None # Changed from n_ctx to block_size for consistency
    # Dropout Hyper Parameters
    C.embd_pdrop = 0.1
    C.resid_pdrop = 0.1
    C.attn_pdrop = 0.1
    C.pretrained_folder = None
    C.n_query_head = None # Added for clarity, as n_head is used for query heads
    C.n_kv_head = C.n_query_head
    return C

  def __init__(self, config):
    super().__init__()
    assert config.vocab_size is not None
    assert config.block_size is not None

    self.block_size = config.block_size
    self.rope = config.rope

    modules = dict(
        wte = nn.Embedding(config.vocab_size, config.n_embd),
        drop = nn.Dropout(config.embd_pdrop),
        h=nn.ModuleList([Block(config) for _ in range(config.n_layer)]),
        ln_f = nn.LayerNorm(config.n_embd)
    )
    if self.rope==False:
        modules['wpe'] = nn.Embedding(config.block_size, config.n_embd)
    self.transformer = nn.ModuleDict(modules)
    self.lm_head = nn.Linear(config.n_embd, config.vocab_size, bias=False)

    # init all weights, and apply a special scaled init to the residual projections, per GPT-2 paper
    self.apply(self._init_weights)
    for pn, p in self.named_parameters():
        if pn.endswith('c_proj.weight'):
            torch.nn.init.normal_(p, mean=0.0, std=0.02/math.sqrt(2 * config.n_layer))

    # report number of parameters (note we don't count the decoder parameters in lm_head)
    n_params = sum(p.numel() for p in self.transformer.parameters())
    print("number of parameters: %.2fM" % (n_params/1e6,))

  def _init_weights(self, module):
      if isinstance(module, nn.Linear):
          torch.nn.init.normal_(module.weight, mean=0.0, std=0.02);
          if module.bias is not None:
              torch.nn.init.zeros_(module.bias)
      elif isinstance(module, nn.Embedding):
          torch.nn.init.normal_(module.weight, mean=0.0, std=0.02);
      elif isinstance(module, nn.LayerNorm):
          torch.nn.init.zeros_(module.bias)
          torch.nn.init.ones_(module.weight)

  def load_pretrained(self, model_path):
      pretrained_state_dict = torch.load(os.path.join(model_path, "model.pt"))
      old_block_size = 64
      with open(os.path.join(model_path,'config.json'), 'r') as file:
          old_config = json.load(file)
          old_block_size = old_config['data']['block_size']
      # Initialize the current model state dict
      self_state_dict = self.state_dict()

      # Loop over the pretrained state dict and update the corresponding weights
      for name, param in pretrained_state_dict.items():
          if name in self_state_dict:
              # If it's the wpe layer and sizes are different, handle separately
              if name == 'transformer.wpe.weight' and param.size(0) != self_state_dict[name].size(0):
                  # Copy the weights for the first 64 neurons
                  self_state_dict[name][:old_block_size, :] = param[:old_block_size, :]
              elif name.startswith("transformer.h.") and name.endswith(".attn.bias") and param.size()[2] != self_state_dict[name].size()[2]:
                  self_state_dict[name][:,:,:old_block_size,:old_block_size] = param
                  # Remaining weights are already randomly initialized
              else:
                  # Copy the weights for layers other than wpe
                  self_state_dict[name].copy_(param)

      # Load the updated state dict into the model
      self.load_state_dict(self_state_dict)

  def configure_optimizers(self, train_config):
      """
      This long function is unfortunately doing something very simple and is being very defensive:
      We are separating out all parameters of the model into two buckets: those that will experience
      weight decay for regularization and those that won't (biases, and layernorm/embedding weights).
      We are then returning the PyTorch optimizer object.
      """

      # separate out all parameters to those that will and won't experience regularizing weight decay
      decay = set()
      no_decay = set()
      whitelist_weight_modules = (torch.nn.Linear, )
      blacklist_weight_modules = (torch.nn.LayerNorm, torch.nn.Embedding)
      for mn, m in self.named_modules():
          for pn, p in m.named_parameters():
              fpn = '%s.%s' % (mn, pn) if mn else pn # full param name
              # random note: because named_modules and named_parameters are recursive
              # we will see the same tensors p many many times. but doing it this way
              # allows us to know which parent module any tensor p belongs to...
              if pn.endswith('bias'):
                  # all biases will not be decayed
                  no_decay.add(fpn)
              elif pn.endswith('weight') and isinstance(m, whitelist_weight_modules):
                  # weights of whitelist modules will be weight decayed
                  decay.add(fpn)
              elif pn.endswith('weight') and isinstance(m, blacklist_weight_modules):
                  # weights of blacklist modules will NOT be weight decayed
                  no_decay.add(fpn)

      # validate that we considered every parameter
      param_dict = {pn: p for pn, p in self.named_parameters()}
      inter_params = decay & no_decay
      union_params = decay | no_decay
      assert len(inter_params) == 0, "parameters %s made it into both decay/no_decay sets!" % (str(inter_params), )
      assert len(param_dict.keys() - union_params) == 0, "parameters %s were not separated into either decay/no_decay set!" \
#                                                   % (str(param_dict.keys() - union_params), )

      # create the pytorch optimizer object
      optim_groups = [
          {"params": [param_dict[pn] for pn in sorted(list(decay))], "weight_decay": train_config.weight_decay},
          {"params": [param_dict[pn] for pn in sorted(list(no_decay))], "weight_decay": 0.0},
      ]
      optimizer = torch.optim.AdamW(optim_groups, lr=train_config.learning_rate, betas=train_config.betas)
      return optimizer

  def forward(self, idx, targets=None):
      attn_times = []
      mem_consumed = []
      device = idx.device
      b, t = idx.size()
      assert t <= self.block_size, f"Cannot forward sequence of length {t}, block size is only {self.block_size}"
      pos = torch.arange(0, t, dtype=torch.long, device=device).unsqueeze(0) # shape (1, t)

      # forward the GPT model itself
      tok_emb = self.transformer.wte(idx) # token embeddings of shape (b, t, n_embd)
      if self.rope==False:
          pos_emb = self.transformer.wpe(pos) # position embeddings of shape (1, t, n_embd)
          x = self.transformer.drop(tok_emb + pos_emb)
      else:
          x = self.transformer.drop(tok_emb)
      for block in self.transformer.h:
          x, attn_time, mem = block(x)
          mem_consumed.append(mem)
          attn_times.append(attn_time)
      x = self.transformer.ln_f(x)
      logits = self.lm_head(x)

      # if we are given some desired targets also calculate the loss
      loss = None
      if targets is not None:
          loss = F.cross_entropy(logits.view(-1, logits.size(-1)), targets.view(-1), ignore_index=-1)

      return logits, loss, sum(attn_times)/len(attn_times), sum(mem_consumed)/len(mem_consumed)

  @torch.no_grad()
  def generate(self, idx, max_new_tokens, temperature=1.0, do_sample=False, top_k=None, eos_token_id=None):
      """
      Take a conditioning sequence of indices idx (LongTensor of shape (b,t)) and complete
      the sequence max_new_tokens times, feeding the predictions back into the model each time.
      Most likely you'll want to make sure to be in model.eval() mode of operation for this.
      Added eos_token_id to stop generation early.
      """
      attn_times = []
      for _ in range(max_new_tokens):
          # if the sequence context is growing too long we must crop it at block_size
          idx_cond = idx if idx.size(1) <= self.block_size else idx[:, -self.block_size:]
          # forward the model to get the logits for the index in the sequence
          logits, _,attn_time,mem_consumed = self(idx_cond)
          # pluck the logits at the final step and scale by desired temperature
          logits = logits[:, -1, :] / temperature
          # optionally crop the logits to only the top k options
          if top_k is not None:
              v, _ = torch.topk(logits, top_k)
              logits[logits < v[:, [-1]]] = -float('Inf')
          # apply softmax to convert logits to (normalized) probabilities
          probs = F.softmax(logits, dim=-1)
          # either sample from the distribution or take the most likely element
          if do_sample:
              idx_next = torch.multinomial(probs, num_samples=1)
          else:
              _, idx_next = torch.topk(probs, k=1, dim=-1)

          # If an EOS token is provided and predicted, stop generation
          if eos_token_id is not None and idx_next == eos_token_id:
              break

          # append sampled index to the running sequence and continue
          idx = torch.cat((idx, idx_next), dim=1)
          attn_times.append(attn_time)

      return idx, sum(attn_times)/len(attn_times)

