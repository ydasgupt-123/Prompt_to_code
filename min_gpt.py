class CharDataset(Dataset):
    """
    Emits batches of characters/tokens from a CSV file, combining input and output columns.
    Supports both character-level and BPE tokenization.
    """

    @staticmethod
    def get_default_config():
        C = CfgNode()
        C.block_size = 128
        C.tokenizer="default" # default or bpe
        C.tokenizer_path = None # Path to save/load BPE tokenizer
        C.bpe_vocab_size = 5000 # Vocab size for BPE training
        C.file_path = '/content/sql_dataset.csv' # Path to the CSV file
        C.input_column = 'text_query' # Name of the column containing input text
        C.output_column = 'sql_command' # Name of the column containing output text
        C.delimiter = ' <SEP> ' # Delimiter to separate input and output in the training text
        C.eos_token = '<EOS>' # End-of-sequence token
        C.pad_token = '<PAD>' # Padding token
        C.unk_token = '<UNK>' # Unknown token
        C.bos_token = '<BOS>' # Beginning-of-sequence token
        return C

    def __init__(self, config, data=None):
        self.config = config

        df = pd.read_csv(config.file_path, engine='python', on_bad_lines='warn')
        all_combined_texts = [] # List to hold all combined strings for tokenizer training
        for _, row in df.iterrows():
            input_text = row[config.input_column]
            output_text = row[config.output_column]
            all_combined_texts.append(f"{input_text}{config.delimiter}{output_text}{config.eos_token}")

        raw_text_for_tokenizer_training = ' '.join(all_combined_texts[:20000]) # Use all data for tokenizer training

        if config.tokenizer == "bpe":
            tokenizer_file = config.tokenizer_path
            # Ensure delimiter is also a special token for proper tokenization and sep_id identification
            special_tokens = [config.eos_token, config.pad_token, config.unk_token, config.bos_token, config.delimiter.strip()]

            if not os.path.exists(tokenizer_file):
                print(f"Training new BPE tokenizer and saving to {tokenizer_file}...")
                bpe_tokenizer = tokenizers.ByteLevelBPETokenizer()
                bpe_tokenizer.train_from_iterator(
                    [raw_text_for_tokenizer_training],
                    vocab_size=config.bpe_vocab_size,
                    min_frequency=2,
                    special_tokens=special_tokens
                )
                os.makedirs(os.path.dirname(tokenizer_file), exist_ok=True)
                bpe_tokenizer.save(tokenizer_file)
                print("BPE tokenizer trained and saved.")
            else:
                print(f"Loading BPE tokenizer from {tokenizer_file}...")
                bpe_tokenizer = tokenizers.Tokenizer.from_file(tokenizer_file)

            self.bpe_tokenizer = bpe_tokenizer
            self.vocab_size = bpe_tokenizer.get_vocab_size()
            self.stoi = {token: bpe_tokenizer.token_to_id(token) for token in bpe_tokenizer.get_vocab()} # Mapping for convenience
            self.itos = {id: token for token, id in self.stoi.items() if id is not None} # Mapping for convenience

            # Now, process each combined text into token IDs for the dataset examples
            self.data = [] # This will now be a list of lists (tokenized examples)
            for combined_text in all_combined_texts:
                self.data.append(self.bpe_tokenizer.encode(combined_text).ids)

            print('Dataset has %d examples. Total tokens after individual tokenization: %d unique (BPE).' % (len(self.data), self.vocab_size))

        else: # Character-level tokenization - similar logic needed
            chars = sorted(list(set(raw_text_for_tokenizer_training)))
            for token in [config.eos_token, config.pad_token, config.unk_token, config.bos_token, config.delimiter.strip()]:
                if token not in chars:
                    chars.append(token)
            chars = sorted(chars)

            self.stoi = { ch:i for i,ch in enumerate(chars) }
            self.itos = { i:ch for i,ch in enumerate(chars) }
            self.vocab_size = vocab_size = len(chars)

            self.data = []
            for combined_text in all_combined_texts:
                self.data.append([self.stoi[s] for s in combined_text if s in self.stoi])

            print('Dataset has %d examples. Total characters after individual tokenization: %d unique (Char-level).' % (len(self.data), self.vocab_size))

    def get_vocab_size(self):
        return self.vocab_size

    def get_block_size(self):
        return self.config.block_size

    def get_eos_token_id(self):
        if self.config.tokenizer == "bpe" and hasattr(self, 'bpe_tokenizer') and self.bpe_tokenizer:
            return self.bpe_tokenizer.token_to_id(self.config.eos_token)
        else:
            return self.stoi.get(self.config.eos_token)

    def get_bos_token_id(self):
        if self.config.tokenizer == "bpe" and hasattr(self, 'bpe_tokenizer') and self.bpe_tokenizer:
            return self.bpe_tokenizer.token_to_id(self.config.bos_token)
        else:
            return self.stoi.get(self.config.bos_token)

    def get_pad_token_id(self):
        if self.config.tokenizer == "bpe" and hasattr(self, 'bpe_tokenizer') and self.bpe_tokenizer:
            return self.bpe_tokenizer.token_to_id(self.config.pad_token)
        else:
            return self.stoi.get(self.config.pad_token)


    def __len__(self):
        return len(self.data) # Number of examples in the dataset

    def __getitem__(self, idx):
      # Retrieve one tokenized example (input + delimiter + output + eos)
      full_tokens = self.data[idx]

      # Truncate or pad to block_size + 1 (for x and y)
      block_size = self.config.block_size
      target_length = block_size + 1

      tokens_to_process = full_tokens
      if len(tokens_to_process) < target_length:
          pad_token_id = self.get_pad_token_id()
          if pad_token_id is None:
              raise ValueError(f"PAD token '{self.config.pad_token}' not found or configured for padding in the tokenizer vocabulary.")
          # Pad at the end
          tokens = tokens_to_process + [pad_token_id] * (target_length - len(tokens_to_process))
      else:
          # Truncate from the end to fit block_size + 1
          tokens = tokens_to_process[:target_length]

      x = torch.tensor(tokens[:-1], dtype=torch.long)
      y = torch.tensor(tokens[1:], dtype=torch.long)

      # We no longer need to return loss_mask
      return x, y

    # Add decode helper for BPE tokenization
    def decode(self, token_ids):
        if self.config.tokenizer == "bpe" and hasattr(self, 'bpe_tokenizer') and self.bpe_tokenizer:
            return self.bpe_tokenizer.decode(token_ids)
        else:
            # Fallback to character-level decoding if not BPE or tokenizer not present
            return ''.join([self.itos[int(i)] for i in token_ids if int(i) in self.itos])

